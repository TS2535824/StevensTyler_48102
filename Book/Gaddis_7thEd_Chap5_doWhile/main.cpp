 //System Libraries 
#include <iostream>   //Input/Output objects 
using namespace std;  //Name-space used in the System Library 
 
 
//User Libraries 

 
//Global Constants 

 
//Function prototypes 

 
//Execution Begins Here! 
int main(int argc, char** argv) { 
    //Declaration of Variables 
    int num=4;
      
    //Input values 
     
    //Process values -> Map inputs to Outputs 
      
    //Display Output 
    cout<<"The variable is "<<num<<endl;
    cout<<"I will now increment the number"<<endl;
   //postfix increment
    num++;
    cout<<"Now the variable is "<<num<<endl;
    cout<<"Lets do it again \n";
    //prefix increment
    ++num;
    cout<<"Now the variable is "<<num<<endl;
    cout<<"Lets do it again \n";
    //postfix decrement
    num--;
    cout<<"Now the variable is "<<num<<endl;
    cout<<"Lets do it again \n";
    //prefix increment
    --num;
    cout<<"Now the variable is "<<num<<endl;
    cout<<"Lets do it again \n";
    
    
 
 
     //Exit Program 
     return 0; 
 } 


